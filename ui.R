library(shiny)

source("ENV.R")
shinyUI(fluidPage(

  # Application title
  titlePanel(paste0("Histogram Demo from ", ENV.NAME)),

  # Sidebar with a slider input for number of bins
  sidebarLayout(
    sidebarPanel(
      sliderInput("bins",
                  "Number of bins:",
                  min = 1,
                  max = 50,
                  value = 30)
    ),

    # Show a plot of the generated distribution
    mainPanel(
      plotOutput("distPlot")
    )
  )
))
